
import java.awt.*;
import java.awt.event.KeyEvent;

public class Controls extends State {
    ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////// Variables ////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    //Background
    private Background background;

    // Text fonts
    private Font titleFont;
    private Font textFont;
    private Font footerFont;

    //Text colours
    private Color colour;

    // Creating about menu
    private int option = 0;
    private String[] menu = {
            "Press enter to return"
    };

    ////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////// Creating Controls /////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////

    public Controls(Manager m) {

        //Creating game state manager in controls
        this.m = m;

        //Setting colour
        colour = Color.WHITE;

        //Setting fonts
        titleFont = new Font("Arial", Font.PLAIN, 28);
        textFont = new Font("Arial", Font.PLAIN, 12);
        footerFont = new Font("Arial", Font.PLAIN, 10);

        //Try to load background graphics
        try {
            background = new Background("Images/background.png", 0);

        } catch (Exception e) { //If loading fails
            e.printStackTrace();
            System.out.println("Error loading control menu graphics.");
            System.exit(0);
        }

    }

    ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////// Menu /////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    // Selecting button to go back to the main menu
    private void click() {
        if (option == 0) {
            m.setState(Manager.MENU);
        }
    }

    ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////// Other ////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    //Creating drawString that recognises line breaks
    void drawString(Graphics g, String string, int x, int y) {
        for (String i : string.split("\n")) {
            g.drawString(i, x, y += g.getFontMetrics().getHeight());
        }
    }

    ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////// Init /////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void init() {

    }

    ////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////// Update /////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void update() {
        //Updating background
        background.update();
    }


    ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////// Draw /////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void draw(Graphics2D g) {
        // draw background
        background.draw(g);

        // set text colour
        g.setColor(colour);

        // draw title
        g.setFont(titleFont);
        g.drawString("Controls", 100, 70);

        // draw main text
        g.setFont(textFont);
        drawString(g,
                "Up: W " + "\n" + "Down: S" + "\n" + "Left: A" + "\n" + "Right: D" + "\n" , 85, 100);
        drawString(g,
                "Sanitiser: K" + "\n" + "Mask: L", 165, 100);

        //draw menu options
        g.setFont(footerFont);
        for (int i = 0; i < menu.length; i++) {
            g.drawString(menu[i], 220, 230);

        }
    }

    ////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////// Key Events ////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    @Override
        public void keyPressed ( int k){
    if (k == KeyEvent.VK_ENTER) {
        click();
    }
        }

        @Override
        public void keyReleased ( int k){

        }
    }


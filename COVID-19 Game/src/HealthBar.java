import javax.imageio.ImageIO;
import java.awt.*;
import java.awt.image.BufferedImage;

public class HealthBar {

    ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////// Variables ////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    private Player player;
    private BufferedImage healthBarGraphic;

    //Text variables
    private Font hbFont;
    private Color colour;

    ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////// Creating Health bar //////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    public HealthBar (Player pc) {
        player = pc;
        //setting font
        hbFont = new Font("Arial", Font.PLAIN, 16);
        //setting colour
        colour = Color.RED;
        //Try to load HB graphics
        try {
            healthBarGraphic = ImageIO.read(getClass().getResourceAsStream("Images/healthbar.gif"));
        } catch (Exception e){ //if fails
            e.printStackTrace();
            System.out.println("Could not load health bar graphics");
        }
    }

    ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////// Init /////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    public void init() {

    }

    ////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////// Update /////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    public void update() {

    }

    ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////// Draw /////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    public void draw(Graphics2D g){
        g.drawImage(healthBarGraphic,110,10,null); // drawing health bar
        g.setColor(colour);
        g.setFont(hbFont);
        g.drawString(player.health() + "/" + player.maxHealth(), 150, 25);
    }
}

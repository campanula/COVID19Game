import java.awt.*;
import java.awt.image.BufferedImage;

public class Animation {

    ////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////// Variables  //////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    private BufferedImage[] frames; //frame array list
    private int current; //the current frame

    private long start; //the animation time
    private boolean played; //if the animation has already been played
    private long delay; //the animation delay

    ////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////// Creating Animation  ///////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    public void Animation(){
        played = false;
    }

    public void aniArray(BufferedImage[] frames){
        this.frames = frames;
        current = 0;
        start = System.nanoTime();
        played = false;
    }

    public BufferedImage ani(){
        return frames[current];
    }

    public void frame(int i){
        current = i;
    }

    public int current(){
        return current;
    }

    public void delay(long d){
        delay = d;
    }

    public boolean played() {
        return played;
    }


    ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////// Init /////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    public void init() {

    }

    ////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////// Update /////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    public void update(){
        if(delay == -1) {return;}
        long elapsed = ((System.nanoTime() - start) /1000000);
        if (elapsed > delay){
            current++;
            start = System.nanoTime();
        }
        if (current == frames.length){
            current = 0;
            played = true;
        }
    }

    ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////// Draw /////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////
    public void draw(Graphics2D g) {

    }

}

